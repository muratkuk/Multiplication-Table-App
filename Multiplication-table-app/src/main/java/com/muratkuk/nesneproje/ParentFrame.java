    private void loadExercises() {
        model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        List<Exercise> exercises = exerciseManager.loadExercises();
        for (Exercise exercise : exercises) {
            model.addRow(new Object[]{exercise.getName(), exercise.getA(), exercise.getB(), exercise.getN()});
        }
    }   
    
