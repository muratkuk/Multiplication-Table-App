    private String[] getCSVFiles(String username) {
        File folder = new File(System.getProperty("user.dir"));
        File[] listOfFiles = folder.listFiles();
        List<String> csvFiles = new ArrayList<>();

        for (File file : listOfFiles) {
            if (file.isFile() && file.getName().startsWith(username) && file.getName().endsWith(".csv")) {
                csvFiles.add(file.getName());
            }
        }

        return csvFiles.toArray(new String[csvFiles.size()]);
    }
    private void updateCSVFiles(String username) {
        csvFiles = getCSVFiles(username);

        csvFileListModel.clear();
        for (String csvFile : csvFiles) {
            String[] csvsp = csvFile.split("_");
            String[] asd = csvsp[3].split(".csv");
            csvFileListModel.addElement(csvsp[0]+" | "+csvsp[1]+" | "+csvsp[2]+" | "+asd[0]);
        }
    }    
